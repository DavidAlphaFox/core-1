    member this.Value =
        match this with
        | Null      -> null
        | Bool    v -> box v
        | Byte    v -> box v
        | Char    v -> box v
        | Double  v -> box v
        | Int     v -> box v
        | Int16   v -> box v
        | Int64   v -> box v
        | SByte   v -> box v
        | Single  v -> box v
        | String  v -> box v
        | UInt16  v -> box v
        | UInt32  v -> box v
        | UInt64  v -> box v
        | Decimal v -> box v
        | ByteArray v -> box v
        | UInt16Array v -> box v


    member this.TSType =
        match this with
        | Null      -> "null"
        | Bool    v -> if v then "true" else "false"
        | Byte    v -> string v
        | Char    v -> "'" + string v + "'"
        | Double  v -> string v
        | Int     v -> string v
        | Int16   v -> string v
        | Int64   v -> string v
        | SByte   v -> string v
        | Single  v -> string v
        | String  v -> "'" + string v + "'"
        | UInt16  v -> string v
        | UInt32  v -> string v
        | UInt64  v -> string v
        | Decimal v -> string v     
        | ByteArray v -> "Uint8Array"
        | UInt16Array v -> "Uint16Array"

